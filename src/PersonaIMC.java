
public class PersonaIMC {

	private float peso; 
	private float altura;
	public float getPeso() {
		return peso;
	}
	public void setPeso(float peso) {
		this.peso = peso;
	}
	public float getAltura() {
		return altura;
	}
	public void setAltura(float altura) {
		this.altura = altura;
	}
	
	public PersonaIMC(float peso, float altura) {
		super();
		this.peso = peso;
		this.altura = altura;
	}
}
